var age = 33;

/*

< - less than
> - greater than
== equals
>= greater than or equals
<= less than or equals
!= not equals

*/

console.log(age < 40);
console.log(age > 40);
console.log(age <= 33);
console.log(age >= 33);
console.log(age == 20);
console.log(age != 20);

console.log();

/*
=== - strict equals
!== - strict not equals
*/

var age2 = "33";

console.log(age2 === 33);
console.log(age2 !== 33);

/*

&& - logical and
|| - logical or

*/

console.log();

var age3 = 45;

console.log(age3 >= 18 && age3 <= 22);
console.log(age3 < 18 || age3 > 65);